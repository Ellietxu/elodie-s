// Nous souhaitons créer des compteur.

// Écris une boucle FOR qui affiche dans la console tous les nombres de 0 à 99.

for (let number = 0; number < 100; number++) {
  console.log(number);
}

// Écris le même compteur avec une boucle WHILE.

let count = 0;

while (count < 100) {
  console.log(count);
  count++;
}

